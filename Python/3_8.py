tourist_places = ['Iceland','Norway','Switzerland','American','Canada']
print(tourist_places)
print(sorted(tourist_places))#临时排序
print(tourist_places)
print(sorted(tourist_places,reverse=True))#临时反向排序
print(tourist_places)
tourist_places.reverse()#反向排序
print(tourist_places)
tourist_places.reverse()#再次反向排序
print(tourist_places)
tourist_places.sort()#永久排序
print(tourist_places)
tourist_places.sort(reverse=True)#永久反向排序
print(tourist_places)