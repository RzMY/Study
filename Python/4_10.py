pizza = ['pepperoni', 'sausage', 'cheese', 'mushroom', 'pineapple']
print("the first three items in the pizza list are:")
for pizza_0 in pizza[:3]:
    print(pizza_0)
print("Three items from the middle of the list are:")
for pizza_1 in pizza[1:4]:
    print(pizza_1)
print("The last three items in the list are:")
for pizza_2 in pizza[-3:]:
    print(pizza_2)