# Q:PEP 8格式设置是什么 A：PEP 8是Python编程语言的编码风格指南，它提供了一些代码格式的规范，
# 以便于Python程序员之间的代码风格保持一致，使得代码更具可读性。
# Q:它的具体内容有哪些？ A：PEP 8的具体内容有：
# 1.使用4个空格作为缩进，而不是使用Tab。
# 2.每行字符数不超过79个。
# 3.使用空行分隔函数和类，以及函数中的大块代码。
# 4.在运算符两边各加一个空格。
# 5.在类名中使用驼峰命名法，即将类名中的每个单词的首字母都大写，而不使用下划线。
# 6.在模块名中，使用小写字母，并在单词之间加上下划线。
# 7.在Python 3中，对于文本，应使用Unicode。
# 8.在Python 2中，对于文本，应使用ASCII编码。
# 9.在Python 2中，对于二进制数据，应使用str类型。
# 10.在Python 3中，对于二进制数据，应使用bytes类型。
# 11.在Python 2中，对于文本，应使用unicode类型。
# 12.在Python 3中，对于文本，应使用str类型。