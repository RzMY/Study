DATA SEGMENT
    CONAME DB 'SPACE EXPLORERS INC.'
    PRLINE  DB 20 DUP (?)
    STUDENT_NAME  DB  'qiuzongsen                    '
    STUDENT_ADDR  DB  '202203792'
    PRINT_LINE  DB  50  DUP(?)
DATA ENDS    
CODE SEGMENT
    ASSUME CS:CODE,DS:DATA,ES:DATA
START:
    MOV AX, DATA
    MOV DS, AX
    MOV ES, AX

    MOV CX, 20 ;设置要复制的字节数
    LEA SI, CONAME+19
    LEA DI, PRLINE+19
    STD ;设置方向标志位，从高地址向低地址移动
    REP MOVSB ;将DS:SI指向的字符串复制到ES:DI指向的字符串

    MOV AL,CONAME+2
    MOV AH, 0

    MOV CX, 30
    LEA SI, STUDENT_NAME
    LEA DI, PRINT_LINE
    CLD ;设置方向标志位，从低地址向高地址移动
    REP MOVSB

    MOV CX,9
    LEA SI, STUDENT_ADDR+8
    LEA DI, PRINT_LINE+49
    STD
    REP MOVSB
    
    MOV AH, 4CH
    INT 21H

CODE ENDS
END START