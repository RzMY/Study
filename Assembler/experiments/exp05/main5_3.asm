DATA SEGMENT
    CONAME DB 'SPACE EXPLORERS INC.'
    PRLINE DB 20 DUP ('$')
    STUDENT_NAME DB '邱宗森'
    STUDENT_ADDR DB '202203792'
    PRINT_LINE DB 50 DUP(?)
    COUNT EQU $-CONAME
DATA ENDS
CODE SEGMENT
    ASSUME DS:DATA,CS:CODE
MAIN PROC
    MOV AX,DATA
    MOV DS,AX

    LEA SI, CONAME
    MOV AX, COUNT - 1
    ADD SI, AX
    LEA DI, PRLINE

    MOV CX, COUNT
    REVERSE:
        CMP SI, OFFSET CONAME  ; 检查 SI 寄存器的值是否小于 CONAME 的地址
        JL END_REVERSE  ; 如果 SI 寄存器的值小于 CONAME 的地址，跳出循环
        MOV AL, [SI]
        MOV [DI], AL
        INC DI
        DEC SI
    LOOP REVERSE
    END_REVERSE:

    LEA SI, CONAME
    MOV AL, [SI + 2]
    MOV AH, 0

    LEA SI, STUDENT_NAME
    LEA DI, PRINT_LINE
    MOV CX, 30
    REP MOVSB

    LEA SI, STUDENT_ADDR
    LEA DI, PRINT_LINE + 30
    MOV CX, 9
    REP MOVSB

    MOV AX, 4CH
    INT 21H
MAIN ENDP
CODE ENDS
END MAIN