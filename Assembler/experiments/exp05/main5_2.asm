DATA SEGMENT
    STRING1  DB  'Visual C++  '
    STRING2  DB  'Visual Basic'
    COUNT   EQU  $-STRING2 ;计算串长度
    MESS1   DB  'MATCH!',13,10,'$'
    MESS2   DB  'NO MATCH!',13,10,'$'
DATA ENDS    
CODE SEGMENT
    ASSUME CS:CODE,DS:DATA,ES:DATA
MAIN PROC FAR
START:
    PUSH DS
    SUB AX, AX
    PUSH AX
    MOV AX, DATA
    MOV DS, AX
    MOV ES, AX
    ; MAIN PROGRAM
    MOV CX, COUNT  
    LEA SI, STRING1
    LEA DI, STRING2
    CLD
    REPZ CMPSB   
    JZ MES1
    MOV DX, OFFSET MESS2
    JMP DISP0
    MES1:  MOV DX, OFFSET MESS1
    DISP0:  MOV AH,9
    INT 21H
    EXIT:   RET  ;返回DOS
MAIN ENDP
CODE ENDS
END START