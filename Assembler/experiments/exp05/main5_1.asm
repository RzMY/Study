     STRING DB 'THE DATA IS FEB&03'
     N DW $-STRING
DATA ENDS
CODE SEGMENT
     ASSUME ES: DATA, CS: CODE
START:
     MOV  AX,DATA
     MOV  ES,AX
     MOV  AL,'&'
     MOV  CX,N
     LEA  DI, STRING
     CLD
     REPNE SCASB
     DEC DI
     MOV STRING[DI],' '
     MOV AH, 4CH
     INT 21H
CODE ENDS
END START